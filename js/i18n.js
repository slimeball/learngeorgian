let currentLang = 'en';
let onLanguageChange = null;

export function getLanguage() {
  return currentLang;
}

export function t(key, vars = {}) {
  const bundle = MESSAGES[currentLang] ?? MESSAGES.en;
  let text = bundle[key] ?? MESSAGES.en[key] ?? key;
  Object.entries(vars).forEach(([name, value]) => {
    text = text.replaceAll(`{${name}}`, String(value));
  });
  return text;
}

export function loadLanguage() {
  try {
    const saved = localStorage.getItem(LANG_KEY);
    if (saved === 'en' || saved === 'zh-TW') return saved;
  } catch {
    /* ignore */
  }
  return 'en';
}

export function setLanguage(lang, notify = true) {
  if (lang !== 'en' && lang !== 'zh-TW') return;
  currentLang = lang;
  try {
    localStorage.setItem(LANG_KEY, lang);
  } catch {
    /* ignore */
  }
  document.documentElement.lang = lang === 'zh-TW' ? 'zh-TW' : 'en';
  applyTranslations();
  if (notify && onLanguageChange) onLanguageChange();
}

export function onLangChange(callback) {
  onLanguageChange = callback;
}

export function applyTranslations() {
  document.title = t('pageTitle');

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });

  document.querySelectorAll('[data-i18n-html]').forEach((el) => {
    el.innerHTML = t(el.dataset.i18nHtml);
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });

  document.querySelectorAll('option[data-i18n]').forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });

  document.querySelectorAll('.lang-btn').forEach((btn) => {
    btn.classList.toggle('active', btn.dataset.lang === currentLang);
  });
}

export function initI18n() {
  currentLang = loadLanguage();
  document.documentElement.lang = currentLang === 'zh-TW' ? 'zh-TW' : 'en';
  applyTranslations();
}
